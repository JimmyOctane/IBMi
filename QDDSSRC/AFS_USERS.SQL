-- Create AFS Users table based on CSV structure
-- File: /home/jflanary/AFS.csv

CREATE TABLE IBMILIB/AFS_USERS (
    ID VARCHAR(20) NOT NULL,
    ACCOUNT_ID VARCHAR(20),
    ACTIVE VARCHAR(10),
    FIRST_NAME VARCHAR(50),
    LAST_NAME VARCHAR(50),
    PHONE VARCHAR(20),
    EMAIL VARCHAR(100),
    ORIG_COMPANY VARCHAR(100),
    ADDRESS1 VARCHAR(100),
    ADDRESS2 VARCHAR(100),
    CITY VARCHAR(50),
    STATE VARCHAR(10),
    POSTAL_CODE VARCHAR(20),
    COUNTRY VARCHAR(50),
    ACCT_BILL_ADDRESS1 VARCHAR(100),
    ACCT_BILL_ADDRESS2 VARCHAR(100),
    ACCT_BILL_CITY VARCHAR(50),
    ACCT_BILL_STATE VARCHAR(10),
    ACCT_BILL_POSTAL_CODE VARCHAR(20),
    ACCT_BILL_COUNTRY VARCHAR(50),
    BILL_CONTACT_NAME VARCHAR(100),
    BILL_EMAIL VARCHAR(100),
    BILL_PHONE VARCHAR(20),
    BILL_ADDRESS1 VARCHAR(100),
    BILL_ADDRESS2 VARCHAR(100),
    BILL_CITY VARCHAR(50),
    BILL_STATE VARCHAR(10),
    BILL_POSTAL_CODE VARCHAR(20),
    BILL_COUNTRY VARCHAR(50),
    ALLOW_QUOTE VARCHAR(10),
    ALLOW_BOOK VARCHAR(10),
    ALLOW_TRACK VARCHAR(10),
    PRODUCT_LIST_MGMT VARCHAR(10),
    VISIBILITY_PORTAL VARCHAR(10),
    SHOW_TRUE_COST VARCHAR(10),
    VIEW_OTHERS_SHIP_QUOTES VARCHAR(10),
    DEFAULT_ORIGIN VARCHAR(50),
    SMARTTMS_ENABLED VARCHAR(10),
    SMART_CLAIMS_ENABLED VARCHAR(10),
    
    -- Constraints
    PRIMARY KEY (ID)
);

-- Create index on Account ID for better performance
CREATE INDEX IBMILIB/AFS_USERS_ACCT_IDX ON IBMILIB/AFS_USERS (ACCOUNT_ID);

-- Add comments for documentation
COMMENT ON TABLE IBMILIB/AFS_USERS IS 'AFS Users imported from CSV file';
COMMENT ON COLUMN IBMILIB/AFS_USERS.ID IS 'Unique User ID';
COMMENT ON COLUMN IBMILIB/AFS_USERS.ACCOUNT_ID IS 'Associated Account ID';
COMMENT ON COLUMN IBMILIB/AFS_USERS.ACTIVE IS 'User active status (TRUE/FALSE)';